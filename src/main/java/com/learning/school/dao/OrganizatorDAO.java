package com.learning.school.dao;

/**
 *
 * @author hromov
 */
public class OrganizatorDAO {
    private Integer id;
    private String name;
    private String phone;

    public OrganizatorDAO(Integer id, String name, String phone) {
        this.id = id;
        this.name = name;
        this.phone = phone;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    @Override
    public String toString() {
        return name;
    }
    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        OrganizatorDAO cl = (OrganizatorDAO) o;
        return id.equals(cl.id);
    }
}
